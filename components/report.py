def generate_pdf():

    report = generate_report_data()

    if report is None:

        return None

    pdf_path = os.path.join(

        REPORT_FOLDER,

        f"{report['report_id']}.pdf"

    )

    doc = SimpleDocTemplate(

        pdf_path,

        pagesize=A4,

        rightMargin=40,

        leftMargin=40,

        topMargin=40,

        bottomMargin=40

    )

    story = []

    # -----------------------------------
    # Pages
    # -----------------------------------

    add_cover_page(

        story,

        report

    )

    add_executive_summary(

        story,

        report

    )

    # Remaining pages
    # add_environment_analysis(...)
    # add_recovery_page(...)
    # add_ai_page(...)
    # add_analytics_page(...)
    # add_smart_map_page(...)
    # add_closing_page(...)

    doc.build(

        story,

        onFirstPage=draw_page,

        onLaterPages=draw_page

    )

    return pdf_path

