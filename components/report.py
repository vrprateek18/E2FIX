LOGO_PATH = "assets/logo.png"


# ==========================================================
# QR CODE
# ==========================================================

def generate_qr(report):

    qr = qrcode.make(

        f"""

E2FIX Environmental Report

Report ID : {report['report_id']}

Location : {report['location']}

Environmental Score : {report['environmental_score']}

AQI : {report['aqi']}

Generated : {report['generated_at']}

"""

    )

    qr_path = os.path.join(

        REPORT_FOLDER,

        "temp_qr.png"

    )

    qr.save(qr_path)

    return qr_path


# ==========================================================
# COVER PAGE
# ==========================================================

def add_cover_page(story, report):

    # -----------------------------
    # Logo
    # -----------------------------
    if os.path.exists(LOGO_PATH):

        logo = Image(

            LOGO_PATH,

            width=2.8*inch,

            height=2.8*inch

        )

    else:

        logo = Paragraph(

            "<b>E2FIX</b>",

            title_style

        )

    # -----------------------------
    # QR Code
    # -----------------------------
    qr_path = generate_qr(report)

    qr = Image(

        qr_path,

        width=1.5*inch,

        height=1.5*inch

    )

    # -----------------------------
    # Logo + QR
    # -----------------------------
    top = Table(

        [[logo, qr]],

        colWidths=[5.3*inch,1.8*inch]

    )

    top.setStyle(

        TableStyle([

            ("VALIGN",(0,0),(-1,-1),"TOP"),

            ("ALIGN",(0,0),(0,0),"LEFT"),

            ("ALIGN",(1,0),(1,0),"RIGHT")

        ])

    )

    story.append(top)

    story.append(Spacer(1,20))


    story.append(

        Paragraph(

            "AI Powered Environmental Assessment Report",

            title_style

        )

    )

    story.append(Spacer(1,10))


    info = [

        ["Report ID", report["report_id"]],

        ["Location", report["location"]],

        ["Date", report["generated_at"].strftime("%d %B %Y")],

        ["Time", report["generated_at"].strftime("%I:%M %p")]

    ]

    table = Table(

        info,

        colWidths=[2*inch,4*inch]

    )

    table.setStyle(

        TableStyle([

            ("BACKGROUND",(0,0),(0,-1),colors.HexColor("#2E7D32")),

            ("TEXTCOLOR",(0,0),(0,-1),colors.white),

            ("GRID",(0,0),(-1,-1),1,colors.grey),

            ("BOTTOMPADDING",(0,0),(-1,-1),8),

            ("BACKGROUND",(1,0),(1,-1),colors.whitesmoke)

        ])

    )

    story.append(table)

    story.append(Spacer(1,25))

    badge = f"""

<b>Environmental Score</b>

<br/><br/>

<font size=28>

{report["environmental_score"]}/100

</font>

<br/><br/>

<b>{report["grade"]}</b>

"""

    story.append(

        Paragraph(

            badge,

            ParagraphStyle(

                "Badge",

                parent=styles["Heading1"],

                alignment=TA_CENTER,

                textColor=colors.darkgreen

            )

        )

    )

    story.append(Spacer(1,20))


    story.append(

        Paragraph(

            """

<b>Generated by E2FIX</b>

<br/>

AI Powered Environmental Decision Support & Recovery Platform

<br/><br/>

<i>

Confidential Environmental Assessment Report

For Educational, Research & Demonstration Purposes

</i>

""",

            ParagraphStyle(

                "Footer",

                alignment=TA_CENTER,

                fontSize=12

            )

        )

    )

    story.append(PageBreak())


